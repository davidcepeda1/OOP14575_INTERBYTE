    public void UploadComboBox(){
        
        MongoCollection <Document> collection = getCollection(PlateController.getNameCollection());
        FindIterable<Document> documents =collection.find();
        MongoCursor <Document> cursor = documents.iterator();
        
        cmbPlate.removeAllItems();
        cmbPlate.addItem("Seleccionar plato");
        
        while(cursor.hasNext()){
            
            Document document = cursor.next();
            cmbPlate.addItem(document.getString("description"));
        }
    }
    
  
    
