    private void btmAgreggateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmAgreggateActionPerformed
        String combo = this.cmbPlate.getSelectedItem().toString();
        if(combo.equalsIgnoreCase("Seleccionar plato")){
            JOptionPane.showMessageDialog(null, "Seleccione un plato");
        }else{
            if(!txfQuantity.getText().isEmpty()){
                if(Integer.parseInt(txfQuantity.getText())>0){
                    
                    quantity = Integer.parseInt(txfQuantity.getText());
                    this.DataPlate();
                    plate = new Plate(id, description, (float) priceUnit, type, quantity, (float) priceTotal);
                    plates.add(plate);
                    JOptionPane.showMessageDialog(null, "Plato agregado");
                    this.UploadComboBox();
                    this.calculateTotal();
                }else{
                    JOptionPane.showMessageDialog(null, "La cantidad no puede ser cero");
                }
                    
            }else{
                JOptionPane.showMessageDialog(null, "Ingrese la cantida del producto");
            }
        }
                this.EnterPlateOnTable();
    }//GEN-LAST:event_btmAgreggateActionPerformed

    public void calculateTotal(){
        subtotalGeneral=0;
        ivaGeneral = 0;
        totalGeneral=0;
        
        for(Plate foundPLate : plates){
            subtotalGeneral+=foundPLate.getPriceTotal();
        }
        
        ivaGeneral = subtotalGeneral*0.12;
        totalGeneral=subtotalGeneral+ivaGeneral;
        txtSubtotal.setText(String.valueOf(subtotalGeneral));
        txtIva.setText(String.valueOf(ivaGeneral));
        txtTotal.setText(String.valueOf(totalGeneral));
    }
    
    
    public void DataPlate(){
        MongoCollection <Document> collection = getCollection(PlateController.getNameCollection());
        FindIterable<Document> documents =collection.find(new Document("description",cmbPlate.getSelectedItem().toString()));
        MongoCursor <Document> cursor = documents.cursor();
        
        
        while(cursor.hasNext()){
            Document document = cursor.next();
            id=document.getInteger("id");
            description = document.getString("description");
            priceUnit = (document.getDouble("price"));
            priceTotal = priceUnit*quantity;
            type = document.getString("type");
            
         
        }
        
    }
    
    
    
    
    private void inicializateTable(){
        modelDataPLate = new DefaultTableModel();
        
        modelDataPLate.addColumn("Id");
        modelDataPLate.addColumn("Descripcion");
        modelDataPLate.addColumn("Tipo");
        modelDataPLate.addColumn("Precio U.");
        modelDataPLate.addColumn("Cantidad");
        modelDataPLate.addColumn("Precio T");
        modelDataPLate.addColumn("Accion");
        
        this.TblOrder.setModel(modelDataPLate);
    }
    
    
    
    public void EnterPlateOnTable(){
        this.modelDataPLate.setRowCount(plates.size());
        
        for(int i=0;i<plates.size();i++){
        this.modelDataPLate.setValueAt(i+1, i, 0);
        this.modelDataPLate.setValueAt(plates.get(i).getDescription(), i, 1);
        this.modelDataPLate.setValueAt(plates.get(i).getType(), i, 2);
        this.modelDataPLate.setValueAt(plates.get(i).getPrice(), i, 3);
        this.modelDataPLate.setValueAt(plates.get(i).getQuantity(), i, 4);
        this.modelDataPLate.setValueAt(plates.get(i).getPriceTotal(), i, 5);
        this.modelDataPLate.setValueAt("Eliminar", i, 6);
    }
        this.TblOrder.setModel(modelDataPLate);
    }
    
    
